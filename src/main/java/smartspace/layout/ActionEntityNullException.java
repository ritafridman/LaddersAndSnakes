package smartspace.layout;

public class ActionEntityNullException extends Exception {

	private static final long serialVersionUID = 2141308524787050104L;

	public ActionEntityNullException() {
		super();

	}

	public ActionEntityNullException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public ActionEntityNullException(String message, Throwable cause) {
		super(message, cause);

	}

	public ActionEntityNullException(String message) {
		super(message);

	}

	public ActionEntityNullException(Throwable cause) {
		super(cause);

	}

}

package smartspace.layout;

public class DoCheckInOnCheckInException extends Exception {

	private static final long serialVersionUID = -4509157954190800760L;

	public DoCheckInOnCheckInException() {
		super();
		
	}

	public DoCheckInOnCheckInException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public DoCheckInOnCheckInException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public DoCheckInOnCheckInException(String message) {
		super(message);
		
	}

	public DoCheckInOnCheckInException(Throwable cause) {
		super(cause);
	}

}
